						<p>This one-woman show in which three distinct Victorian ghost stories are related to the audience, is almost entirely dependent on the performance of Rebecca Vaughan, and she is amazing. She is by turns the amused, detached observer and the terrified participant in a series of stories which play with the conventions of Victorian storytelling – the disappointed lover, the scientist whose creation destroys him, the family torn apart because they failed to act in time. More deeply ingrained Victorian ideas are also in evidence here: artists are fickle, foreign medicines are magic and men are obsessed with keeping women ignorant.</p>

						<p>Each story is different and each evokes a very old-fashioned type of creeping horror in the audience, dependent on psychological cues rather than shocks and gore, and each plays into a real fear, be it fear of rejection, medicine going wrong or ignorance creeping out of the cupboard in the corner to kill.</p>

						<p>Light and sound are used to great effect throughout the performance, evoking a number of different atmospheres, from a crypt to a laboratory, without any set changes whatsoever. Vaughan is also excellent at summoning additional pieces of set entirely through mimed actions, and in conveying one thing through tone and quite another through body language. Watching this piece is a master class in storytelling. Chilling, haunting, dazzling, ‘Female Gothic' is a real delight. Brilliant, brilliant work.</p>
