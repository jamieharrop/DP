						<p>Ghoulishly good</p>

						<p>Female Gothic is a treat of a show for anyone as macabre-minded as myself; but then again I compulsively watch plane crash documentaries. Scratch that, it was a cracking show for anyone who appreciates suspenseful storytelling with a delicious tingle of horror; Rebecca Vaughan managed to make her show impressively scary for a brunch time slot.</p>

						<p>I had half expected re-hashed versions of classic Gothic tales, such as Bluebeard, or equally an amalgamation of wailing maidens plus locked doors plus things that go bump in the night - elements that crop up in your standard, humdrum, run-of-the- mausoleum Gothic story. So I was pleasantly surprised to find that all three stories were unfamiliar to me.</p>

						<p>The first was a tale of a dead girl promised in marriage and subsequently abandoned, coming back to haunt her faithless lover. It reminded me of Emile Zola's Therese Raquin, with its eerie portrayal of the dead coming tangibly back to Earth in order to wreak their vengeance. The second owed a lot to The Curious Case of Doctor Jekyll and Mr. Hyde with its scientific elements, exploring the fundamental concerns that arise when man plays God. The final story was introduced as a personal memory of the narrator and was perhaps the most unsettling due to its denial of any kind of explanation or closure. In essence it was a jazzed-up haunted house yarn, but Vaughan brought some unexpected elements and twists, alongside a truly visceral sense of horror, to the narrative.</p>

						<p>For a morning show with all the shivers of a midnight witching hour, Rebecca Vaughan is well worth a watch. Don't get nightmares...</p>
