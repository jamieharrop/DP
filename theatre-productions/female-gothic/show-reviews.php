						<p>If you are looking for a handsome, well-turned out, value-for-money Fringe show, that will offer you a superbly professional 75 minutes of pre-lunch entertainment without rearranging your life in any way, then Dyad Productions's Female Gothic is exactly the show for you. Written and beautifully performed by Rebecca Vaughan, in a most elegant Victorian dress, it tells three Gothic tales of supernatural horror, each linked by a faint sense of the ongoing 19th century battle of the sexes.</p>

						<p>So in the first, an arrogant student is pursued across Europe by the unquiet spirit of the loving young fiancee he jilted. In the second, an obsessive scientist is condemned to eternal horror by the passion for experimentation that led him to reject the woman who loved him. And in the third, the narrator tells a tale from her own life, of a friend seized by death soon after giving birth to her first child, and of the friend's distraught husband, whom the narrator perhaps once loved.</p>

						<p>All three tales are delivered with impressive eloquence and quiet passion, in a ladylike but intense performance that holds the audience in the palm of its hand.</p>

						<p>And if 11.45 in the morning is a strange time to be enjoying this near-perfect piece of after-dinner theatre, it still offers a great deal of pleasure, to those who like their theatre polite and handsome, but not without an edge of sharp intelligence.</p>
