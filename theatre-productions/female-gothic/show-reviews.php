						<p>From the comfort of a creaky Victorian armchair, in an unsettling space barely lit by few flickering candles, a lone woman proceeds to tell three haunting tales of the terrifying and sublime – quite a fitting scene to unfold in Edinburgh, the city of literature, on this typically gloomy day.</p>

						<p>Adapted and performed by Rebecca Vaughan, Female Gothic is a celebration of all things uncanny, sourced from the pages of some of Gothic fiction's forgotten female writers. Each of the chosen stories explores overarching themes familiar to the gothic genre such as hubris, monstrosity and the unknown, while at the same time transcending the genre roots to reveal a deeper anxiety of women writers, during a time when changes in social norms had become a platform for fear.</p>

						<p>Vaughan's performance is remarkable. She transports the audience through time, bringing places and characters to life with few sound effects and a masterful use of the vivid and voluptuous Victorian language. As a regular period performer, Vaughan adopts the role of a haunted lost soul with ease, owning the mannerism and insight that one would expect from a noble woman during the era. She makes clever use of the minimal set pieces, consisting of a goblet, and armchair and three solitary standing candles, adding more to the experience than a simple re-telling of gothic fiction. Ultimately The Female Gothic succeeds due to its creation of a distinctly unsettling atmosphere that has the audience lingering on Vaughan's every word. There are several moments of spine-tingling terror which arise from a consistent accumulation of general creepiness. An incredibly refined one-woman show, Female Gothic will have you captivated from start to finish.</p>
