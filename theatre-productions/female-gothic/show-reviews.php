						<p>This one woman performance piece by Dyad Productions directed by Guy Masterton and starring Rebecca Vaughan- who wrote the piece- is as impressive and moody a tale as you are likely to encounter in a midday lunchtime slot. Vaughan carries the three separate tales more than adequately and uses nuance and skill as a major part of her storytelling style whilst capturing the right amount of melancholia without ever dipping into the overly theatrical many would associate with Gothic writing.</p>

						<p>Taking the form of three short stories Vaughan begins by taking to the sparse but effective set with a leather wingback chair, subtle yet dramatic lighting which creates the right level of shadow and then lighting three candles –one is snuffed out every time she concludes an episode and this indicates another tale is about to unfold- begins the first of her Victorian themed pieces.</p>

						<p>The stories themselves twist and turn with dramatic flourish as is needed. With very little assistance apart from a few sound effects –dogs howling in the distance, the swell of music as her character approaches the Paris Opera House- Vaughan carries the tales with aplomb creating the necessary ambience with her storytelling abilities alone. The subtle shifts in the lighting are the only other assistance she requires and these are so subtle as to be hardly noticeable.</p>

						<p>The script itself is involved without being complicated. Likewise Vaughan's performance could hardly be more fully realised or capable. Never faltering or showing any signs of hesitation she manages to make it through the one hour and fifteen minutes making it seem quite effortless indeed.</p>
