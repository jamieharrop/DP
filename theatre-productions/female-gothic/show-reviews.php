						<p>It's hard to trade a beautiful, sunny day for the gloom of the theatre. But when the show you're seeing is as enjoyable as Rebecca Vaughan's thought provoking, delightfully creepy one-woman production, Female Gothic, the sacrifice is well worth it.</p>

						<p>You might think that you're just going to settle in for a trio of Gothic ghost stories from three Victorian authoresses, but Vaughan, who both wrote and stars in the piece, knows that there's much more to these tales than a few chills. She makes sure that the audience knows it as well. Victorian-era prejudices – artists are untrustworthy, South Sea islanders are magical, women should be religious – are all on show, along with some interesting, almost early-feminist observations on the weakness of men. The latter are portrayed as fickle, arrogant, or unnecessarily obsessed with keeping women ignorant; always, of course, to their detriment. As she segues between stories, Vaughan offers some wry observations on imagination, love, and scientific curiosity, all of which are fine, as long as they aren't taken too far.</p>

						<p>As writer and star, the show rests squarely on Vaughan's shoulders. She carries is beautifully, chatting with the audience as if they'd just stumbled into her sitting room for the evening. Her acting was never overdone (always a danger when an actor's working in an intimate space and dealing with occasionally overblown language and dramatic situations). The grandiose language and descriptions that characterize Gothic writing translate surprisingly well to the stage; I found myself hearing the stories recited more than I ever did reading them.</p>

						<p>Female Gothic celebrates women; not just the words three ladies put on the page, but women's lives during a particular period in time. Many women writers during the Victorian era took to the Gothic form as a subversive way to expressive very real-world fears – abandonment, forced marriage or death in childbirth – and their frustrations with a patriarchal society that forced them to act frail or ignorant. On the face of things, these are just fantastical ghost stories, but if you look a little deeper there's much more going on. Vaughan gets that fact, and clearly hopes you will too, thought perhaps in your own time. After all, as she herself observes, there's "no shortcut to wisdom".</p>
