						<p>With Austen's Women and I, Elizabeth under her bustle, Rebecca Vaughan has shown she's a dab hand at putting together a classy one-woman show. Female Gothic is no different. Vaughan is reclaiming the female voices of the Victorian spiritualist genre, who have been buried by their male counterpoints in the annals of history.</p>

						<p>Female Gothic is elegantly put together, and performed with conviction by this impressive actor. Capturing the sombre omniscience of all good narrators, she tells us of betrayals that lead to supernatural deaths, of scientists who, in trying to be gods, are punished by them, and finally a spooky story from her own history.</p>

						<p>It's pretty gloomy, as one would expect, but there's enough richness in Vaughan's adaptations to keep you listening. It's undeniably atmospheric, and the images that Vaughan evokes in the theatre linger for a while after you've left her dark parlour, haunting you still.</p>
