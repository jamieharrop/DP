						<p>Rebecca Vaughan, in period costume, steps into the skin of fourteen of Jane Austen's female characters, bringing them to entertaining life, with sharp observation, clever imagination and well chosen moments of humour and philosophical intensity, much to the delight of an audience that rewards the startling feat of performance, portrayal, writing and direction with loud applause at the end.</p>

						<p>The staging is simple; a dresser, a screen, and Vaughan using skilled vocal work, eye theatre, movement and gesture to bring to this audience, sans fourth wall, fourteen women - Lizzy Bennet, Emma Woodhouse, Mrs Norris, Mary Musgrove to name just a few. "Emma" seems to be a particular favourite of Vaughan and is the most represented. The simplicity of staging and direction allows the characters to come forward and we are soon in their midst, in the room with them, enjoying their opinions and orientations towards the lives they live. Vaughan speaks directly to us, the words are enough and it is a strength of this production that it hasn't been over-choreographed. The directness of it is outstanding.</p>

						<p>There's much to savour and enjoy. The tone is light, the narrator is witty and enjoys describing and introducing her characters. They become examples of women in a narrow part of society that Austen chose to write about, often self-centred, lost, often seeking, occasionally bemused at life's occurrences - always enjoyable to watch. There's a warmth to this selection which never becomes over-sentimental.  A young Emma Woodhouse, twenty-one, who is rich and stricken with "the power of having rather too much her own way", is a particular personal favourite. Vaughan seems to inhabit the skin of each with consummate ease and a charisma that holds the attention throughout.</p>

						<p>A real strength of this piece is that Vaughan, and Masterson as director, have stayed the right side of caricature; each character has been carefully created, delineated from the others, and each is a piece in itself. The narrative does not interfere but serves the flow of the production. We're guided, we're addressed, we're sometimes educated! But we're always entertained.</p>

						<p>Vaughan is a hugely competent actor, and the whole hour rushes by. This is a strongly recommended show and I am not sure there's anything else quite like it at this year's Edinburgh Fringe.</p>
