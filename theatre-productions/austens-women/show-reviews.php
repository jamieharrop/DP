						<p>Austen's Women takes a rewarding exploration of her varied female characters, leaving aside the focus of the men, which is refreshing as everyone's sick of Colin Firth's Mr. Darcy overshadowing Austen's caustically witty writing.  Jane Austen aficionado Vaughan has pieced together various writings from Austen's catalog, focusing on the all-important narrator who is allowed the luxury of observing and commenting without biased, interlaced between reflections from a mixture of thirteen of Austen's most memorably astute and foolish characters.</p>

						<p>As we arrive, the sound system is playing 60s and 70s pop including 'Sugar Baby Love' by The Rubettes, which sets up beautifully the playful approach of Vaughan's delivery and Masterson's direction. We are taken through moments of elevated humour to touching scenes of pathos and isolation, Vaughan shifting from each character with ease only aided by a dimming of lights between character and narrator.</p>

						<p>Vaughan effectively explores the plight of women as they have to constrain their mores in Austen's era and delightfully modernises the easily identifiable woes that women still face today, from love to social etiquette, or lack thereof!</p>

						<p>The audience delighted in Vaughan's performance which was unfaultable, drawing us into the scenes by addressing us directly and assuring we were included in the subtle wit, boisterous idiocy and heartache of every moment.  Vaughan's turn as each character is superb as she handles the matriarchal madams like Mrs. Norris, the young and naive like Catherine Morland and the strong-willed, non conformists like Elizabeth Bennet.</p>

						<p>Each character is fully realised, the inane Miss Bates being a particular highlight along with a hilarious recital of nasal-twined Mary Stanhope which is counter-acted with a moving portrayal of a distraught Marianne Dashwood.  This is a thoroughly enjoyable exploration of women, who it is clear to see, have not changed their mentality much in the last 200 years when it comes to men. Beginning and ending with Elizabeth Bennet as her pride and prejudice wane in favour of Darcy, this highlights the productions full circle exploring the civility, deference and politics of Austen's time, and is thoroughly recommended to any current or wannabe Jane Austen fans. </p>
