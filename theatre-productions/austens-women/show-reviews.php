						<p>There is quite a lot of Jane Austen at the Fringe this year, and if Austen's Women is representative, then we're in for a real treat. The show is a compilation of monologues by thirteen of Austen's heroines. Beginning with Lizzy Bennet, we move on to hear the words of Austen's famous and lesser known female characters, including Mrs Norris, Miss Bates and Emma Woodhouse. The monologues are interspersed with commentary highlighting aspects of the female character according to Austen, from folly, to shrewdness, to generosity, vanity and prudence. The show opens with Austen's comments about the inconstancy of women, a theme and literary tradition inherited because, as we are immediately told, men had the advantage in telling the story.</p>

						<p>Austen's Women is written using only Austen's words, but the characters are stripped of their context. Dialogue becomes monologue, and words once said within company become private moments of reflection. Most importantly, these women are stripped of their men. One actress, Rebecca Vaughan, delivers the entire performance. She appears bare-footed, dressed in a modest period undergarment, and moves from one character to the next with a subtle change of clothing. The simplicity of the stage, set with only a desk, reinforces the notion of peeling away the layers, and creates a feeling of intimacy. Vaughan's performance is seamless, and the play shows off her skilful ability to convey character. The ease with which she flits from person to person, embodying thirteen women in one hour, is impressive. She truly captures the wit and humour of Austen's writing and the eccentricities of her characters. Her facial expressions, voice and body language are captivating and at times hilarious, making this a highly entertaining and enjoyable show.</p>

						<p>By removing these characters from their context, Dyad Productions seeks to present a ‘distillation of nineteenth century feminism', highlighting its contemporary relevance. The production weaves together moments of comedy with more serious revelations about the social realities and pressures faced by women. You will find yourself laughing at Mrs Norris's warnings about stepping out of rank and Mary Stanhope's naïve considerations of a marriage offer, while feeling the sadness of love, lust and rejection, as Marianne Dashwood sits on the floor, sobbing.</p>
