						<p>A Masterful One-Woman Show:<br />
						Austen's Women is a most agreeable tribute to the enduring and much-loved author Jane Austen. Rebecca Vaughan has adapted the words of and performed the characters from the works of the 19th century romantic novelist in a manner to be most universally admired. For those who have been living in solitary confinement all of their lives, Jane Austen was an English novelist whose romantic fiction novels are amongst the most widely read in English literature. Her works and her life have spawned numerous modern-day fan clubs, films and television adaptations.</p>

						<p>Literary critics continue to debate her works to this day. In a masterful one-woman show Vaughan depicts 14 female characters from the much-loved works. She fluidly moves from one character to another appropriately starting and ending with arguably the most popular - Elizabeth Bennett from "Pride and Prejudice'. Director Guy Masterson has helped Vaughan bring to life a kaleidoscope of colourful characters, calling forth precious memories for fans and whetting the appetite for others. It is not just the characters that light up the stage, but also the dry, humorous and sarcastic words of Austen that come to life. Who can forget the infamous opening line of 'Pride and Prejudice'? - "It is a truth universally acknowledged, that a single man in possession of a good fortune, must be in want of a wife.'  It is on this premise that Masterson and Vaughan base their show's theme - marriage and the importance of it in Austen's society and consequently her literary works.</p>

						<p>Each character is subtly, or not so subtly, manoeuvring their lives around the choosing of or the consequences of having chosen a husband, or is facing a life without a husband. Austen's novels are alive with sparkling characters both men and women. This show is about the women, the degrees to which the choice of a marriage partner affects their lives and how they maintain their self-respect (or not) in doing so.</p>

						<p>Vaughan superbly switches from Regency character to character - one moment the wretched, love-lorn Marianne Dashwood from 'Sense and Sensibility', next the hard-hearted Mrs Norris of 'Mansfield Park' and then the ditzy spinster Miss Bates from 'Emma'. This is to name just a few of the 14 wonderful characters.</p>

						<p>This is a one-woman show for Austen fans to relish. It is a treatise on women and marriage in the 19th century in exactly the manner of which Ms Austen would have approved. It is also an excellent first impression for those who have not yet had the delight of being introduced to the delightfully wicked and witty world of Jane Austen.</p>
