						<p>For their 2014 Festival Fringe production, as we commemorate the start of the Great War, it is timely to present a stage version of Virginia Woolf's modernist masterpiece, Mrs Dalloway.</p>

						<p>Woolf parallels a single day, 13 June, 1923, in the lives of two people: the privileged, socially elite wife of an M.P., Clarissa Dalloway, and Septimus Warren Smith, a shell-shocked veteran of the First World War. As the day begins Clarissa is buying flowers for a party she will give that night, while Septimus is in Regent's Park listening to the sparrows, who, he believes, sing to him in Greek.</p>

						<p>They each encounter several people, whether in reality, thoughts or memories, a host of characters drifting in and out through the free-flowing monologue. Narrated from the perspective of Mrs Dalloway, it's been imaginatively dramatised as a one-woman play, starring Rebecca Vaughan.</p>

						<p>The minimalist stage set reflects a drawing room of a grand London Townhouse: long drapes represent three tall windows, beside a white chaise longue.</p>

						<p>Dressed in an elegant emerald green chiffon tea-dress, Clarissa, tall, slim, poised, smiling with a sense of joy, explains that she must go out to buy flowers for her party. The beauty and scent of fresh flowers, “The War is over,” she sighs with relief - she can look forward to peaceful London life again.</p>

						<p>For Septimus, back from the trenches in France, his personal war of suffering continues; even the comforting love of his wife Lucrezia, cannot ease his tortured mind. With a quick change of voice, her arm shaking against her thigh, Rebecca portrays Septimus, his anguish, anxiety, fear expressed with such pathos.</p>

						<p>Clarissa leads us on a journey around Westminster but also in her mind's eye back in time: sad memories of happier days, lost loves - Peter Walsh, whom she declined to marry, then went off and met a "pretty nincompoop on a boat to India!"</p>

						<p>A heartbreaking scene is an anecdote of youthful innocence, her schoolgirl crush for Sally Seton, sharing poetry, "Plato for breakfast and Shelley hourly." Given a flower and a kiss, the bud of a love affair does not blossom.</p>

						<p>We hear about Clarissa's quiet marriage to Richard and introduced to her society friends, Lady Bruton and Hugh Whitbread. Here's Peter himself, back in town and kindly come to see her. He strides across the room, hands in pockets, a throaty laugh. After all these years, does he still love her?</p>

						<p>From Richard and Peter, her daughter Elizabeth to Septimus's Doctor Bradshaw, the characters are a series of miniature cameo portraits.</p>

						<p>Theatrically it works brilliantly following Woolf's literary style. Mrs Dalloway describes her own feelings and emotions but like a 'stream of consciousness' she gives the impression of being inside the mind of the other characters, their interlinking thoughts woven like a spider's web.</p>

						<p>The sense of time ticking away through the course of the day is emphasised through this well-paced production: the chiming of Big Ben as Clarissa walks down Bond Street and visits the florist. Late afternoon, she must get ready to greet her guests.</p>

						<p>The simplicity of design, subtle lighting, clock bells, bird song, piano music all create an effective soundscape, period setting and romantic mood.</p>

						<p>The concise, crisp editing of the text is complemented by delicate, seamless direction. Elton Townend Jones has distilled Mrs Dalloway into a perfect gin martini – dry, smooth, sophisticated.</p>

						<p>Don't be Afraid of Virginia Woolf! Mrs Dalloway has been brought colourfully to life in this richly re-imagined, exquisitely evocative stage adaptation, portrayed by Rebecca Vaughan with heart-breaking poignancy and passion. </p>
