						<p>Great cocktail parties are increasingly rare. DALLOWAY, at the Eternity for the Sydney Writers Festival is the rarest affair of all. A gentle and gracious soiree where the fine conversation flows and the guests are eclectic and interesting to meet. As you head home stimulated by the ideas discussed and the emotions touched, your mind turns to the hostess. Is she what they say she is or what she appears to be? And… did she buy those flowers herself?</p>

						<p>Based on Virginia Woolf's Mrs Dalloway, DALLOWAY distills the essence of the novel like a fine brandy. It is a hot June day in post war London, 1923, and Clarissa Dalloway, sunny and gay, is preparing a party at her home in Westminster. She likes to succeed. Her silly little life has had many social triumphs and this will be no exception. The Prime Minister is coming…a great honour.</p>

						<p>In a park nearby Septimus Warren Smith is her opposite. He is brooding and troubled. His late onset of ‘shell shock' is scaring his Italian wife Lucrezia. He believes the sparrows are talking to him and his new doctor plans on putting him in mental institution.</p>

						<p>Woolf is a writer who is often adapted. WOOLF WORKS, the extraordinary ballet, is playing at the Royal Opera House and this particular novel was given the big screen treatment as the context for award winning 2002 film, THE HOURS, the original title of the novel. This adaptation by UK's Dyad Productions is also lovingly faithful to the themes and style of the original.</p>

						<p>Stream of Consciousness writing does lend itself well to being spoken but writer-director Elton Townend Jones takes on the difficult task of further translating the work into theatre. His is a deceptively simple technique…. speak the 3rd person sections as if they were 1st person speech and direct them questioningly at the audience.</p>

						<p>Choose where the prose engages with the characters and have the character deliver, not just the direct speech as dialogue, but the context, foreshadowing, exposition, and sometimes the aftermath. In the skilled hands of the single performer, we sometimes feel the pain and joy of the person we have just met through the eyes of another. Like whispered gossip at small intimate gathering.</p>

						<p>Time and place are easily established though the words of the novel. ‘My London, omnibus, roll of tweed, unspeakably' contextualise the performance. Well executed tech, Big Ben, a chiming clock and simple lighting changes of focus or colour, add variety and propel Clarissa's day. But it is Rebecca Vaughan who does the work for 90 minutes to bring Septimus to his sad end and Clarissa to her moment of deepest reflection?</p>

						<p>Vaughan is simply wonderful. There are 20 or so characters in the novel and not all are there onstage but we really don't need to meet her daughter Elizabeth or her Aunt Helena. The women we do meet are clearly delineated by voice and accent and intent. The eternal question in theatre is: can women successfully play men? Vaughan can.</p>

						<p>The physical craft is there. She spreads her stance and pontificates or lounges with one leg forward and changes the timbre of the voice for the upper-class ex-lover. She hunches ever so slightly and poor disturbed Septimus is alive for us.</p>

						<p>She is brilliantly supported by a wonderful dress which allows her men to pull at a cuff, or hold a lapel or thrust hands in pockets without destroying the beauty of the gossamer insert of the front pleats. The sheer sleeves float when she uses her arms in fluidly mimetic ways to introduce a character to another, to elucidate an emotion or appeal to the audience to approve her thinking. And her mime is so well crafted. Sewing or taking tea or an illicit kiss.</p>

						<p>But it is Vaughan's emotional range which makes the characters believable and recognizable so that the story can sweep along. Peter's comments about Clarissa are cruel but accurate and reflected from the beginning to be brought to the surface by Clarissa's hollow triumph.</p>

						<p>The ‘profound darkness' of a generation who will repeat their errors is writ large in the arrogance of the doctors and the inability of Richard Dalloway to express his interior life. The ending of Septimus' story is heart wrenchingly powerful yet she slips easily into facileness in a breath. The perfect hostess for Woolf's creations.</p>

