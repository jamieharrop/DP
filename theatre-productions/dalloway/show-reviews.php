						<p>Probably one of Virginia Woolf's best-known and most loved novels, Mrs Dalloway shares a day in the life of Clarissa Dalloway, a woman from post-First World War London's high society. Dalloway's life is brought to us through her reflections on her past and some of the characters that have shaped her outlook on the world.</p>

						<p>The stage is bare, cream white, with just a chaise-longue, evoking rich society London. This is a character study with a difference. Rebecca Vaughan, writer and performer, as she did with Austen's Women, lifts a character already created in fiction by another writer, and transposes it onto the theatre stage. With rich source material, and her own proven ability to draw the essence of popular characters from literature into live performance, we have Mrs Dalloway before us.</p>

						<p>Dalloway is our storyteller, our guide through London, past the omnibuses in Piccadilly, a woman “far out to sea”, a woman who “dresses well, but spends little”, a woman with a flair and time for self-observation and commentary on those in her milieu. Vaughan plays a host of characters, delineating them well, and filling the stage with not just Clarissa, but her immediate world, of Peter Walsh, Septimum Warren, Lucretia and many more.</p>

						<p>This is yet another highly distinguished piece of solo theatre from Dyad Productions – a finely crafted and honed monologue (written and directed by Elton Townend Jones) that captures the essence, remoulds and then sets free anew the words of Virginia Woolf, honouring the original, yet playing with it in a way only theatre allows, lending the work a theatrical dynamism, ranging from the comedy of wry observation and irony, and the intensity of confronting mortality and one's darker self. On stage we see Clarissa before us, immerse ourselves in her London, her realm of experience. Without a prop in sight, and within moments, we are all in there.</p>

						<p>Staged fairly statically, what moves, what flies and swirls is the narrative, always on course to deliver the tale, but also cruising into different episodes, arrivals and realisations. Vaughan is a master at her craft. What craft is that? It is multi-faceted and has been honed by Vaughan over several years: character interpretation and portrayal, direct and bare performance, eloquence and intelligence, the courage to stage simply without fuss, precision in gesture and emotional presentation, storytelling that lays relaxed, easy claim to humour, introspection shared publicly, dramatic tension, and shifting mood and playing different characters, older and younger, male and female.</p>

						<p>Vaughan the actor, and Townend Jones the writer and tranposer, lift Woolf off the page and present Mrs Dalloway before us with bold, accessible honesty. One never doubts that Rebecca Vaughan is living the character with every cell of her body. She remained completely focused, rarely missing a beat for eighty minutes. Entirely Dalloway, there is no hint of Vaughan. This is overshadowing and then incarnation into a character which is rare in theatre. Townend Jones adds to the immediacy by giving us a Clarissa speaking in the present tense.</p>

						<p>In places, Dalloway may feel text heavy, too long for some, but that is because theatre like this has a right to demand attention, to expect active listening. To enter the world of Dalloway one has to go through the door of attention willingly, because this character is so detailed, her story so laden – not only with narrative – but also reflection. There's texture in Dalloway and you'll miss much of it, if you sit back and wait.</p>

						<p>There's storytelling at the heart of Dalloway, but also an underlying ability to both tell and be that story. The moments of humour in it generate plenty of laughter and there's scope for a bit more in the script to lighten and temper its intense elements.</p>

						<p>For five minutes I closed my eyes, radio-play style – and there she was, clear as the bells of London: Clarissa Dalloway, even more alive and present. This is solo theatre rooted in acting and delivery whose light reaches into the furthest corners of the theatre space.</p>

						<p>The Fringe in Edinburgh is very long, almost as long in days as a short tour. I saw Dalloway on its final day at the Fringe and this review is focused on that performance. As an artistic thing, this felt like burnished gold, something crafted and polished to near perfection and ready to tour to many places. This is an outstanding production that renders Woolf for the stage in a way that honours the original but also enhances it with Vaughan's now expected tour-de-force character acting, tightly and intelligently directed. </p>					
