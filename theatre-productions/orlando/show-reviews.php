				<p>It is a huge challenge for any actor to perform on the hottest day of the year. The sweltering discomfort is increased by lighting and heavy costumes while the uncomfortable audience are harder to win over.  However in Orlando, performer Rebecca Vaughan proved herself to be a consummate professional by overcoming all these setbacks, delivering a fantastic one woman show for a straight 90 minutes. </p>
				
				<p>Not once in this epic play did she lose pace or focus in spite of wearing several layers of costumes and portraying multiple characters with no convenient exit and the chance for a breather in the wings! </p>
				
				<p>Virginia Woolf's Orlando, supposedly inspired by the androgynous, aristocratic Vita Sackville-West, is considered by many a challenging book.  It is a biography of a character that after a blessing from Elizabeth I, has a magically long life spanning four centuries, ending in the 1920s.  During this time, Orlando, an aspiring poet, undergoes many transformations, including gender which gives the reader a unique insight into the developing political, sexual and class horizons. </p>
				
				<p>It is a story rich in opportunities for the right person to adapt for the stage. Playwright/director Elton Townend Jones has more than risen to this challenge. His interpretation is funny yet challenging and brings the story right up to date. I liked the way he changed the narrative style from biography to autobiography – in the context of a monologue, the story becomes an intimate confessional between character and audience.</p>
				
				<p>"If I am rambling, the fault is yours for listening to someone talking to themselves!"</p>
				
				<p>This approach works particularly well in a small friendly studio space such as the Maltings. Perhaps this is why I felt that this play made sense of the book for me. Orlando's personal journey is everything and the audience were a part of it. 
				Townend Jones urged his audience to "take from it what you will and take as much as you can." </p>
				
				<p>The theme of transformation was not only realised via the text but in the staging too, in which set became costumes and vice versa.  Kate Flanaghan, costume designer, inventively used layers and multifunctional accessories to take Orlando from Tudor times to 2018. </p>
				
				<p>If I have one reservation it is that this intimate piece might have been even more effective performed in the round or as a thrust stage.  However with a company who clearly have a collaborative experimental ethos, I expect this had been ruled out for a good reason.</p>
				
				<p>Orlando is not only an interesting outing for literature, history and psychology buffs but is a "must see" piece of theatre for anyone who likes edgy comedy. Furthermore, it is a chance to see a strong, immensely versatile female performer take full command of her stage. </p>
				
				<p>I congratulate Vaughan and Townend Jones on a highly successful collaboration. Dyad Productions appear to be going from strength to strength and I feel sure that their forthcoming tour and stint at the Edinburgh Festival (Assembly Roxy Upstairs, August 2-27) will be a huge success.</p>
