						<p>It's fitting in many ways that, upon their tenth year visiting the Fringe, having brought as many plays to the festival stage, a company known for its mercurial takes upon classic texts and subjects would opt to focus on a book that is in essence the distillation of their particular style.</p>
						
						<p>Peering into Virginia Woolf's satirical novel, <em>Orlando: A Biography</em>, Dyad brings the struggle of the artist's quest for truth to the stage, wrapped in an adventure through love, identity, sexuality and self.</p>
						
						<p>A tale of a seemingly immortal struggling poet, born into affluence and position, as at times, he, and other times she, travels and loves his way from the court of Elizabethan England, through the intrigues of Constantinople, to the bustling streets of modern London, falling in and out of love, meeting with famed writers, and working on his great poem, The Oak Tree.</p>
						
						<p>Woolf's novel was written ostensibly as a satire of the plight of writers throughout history as well as being a sly and winking dig at the familial history of her friend, fellow writer and sometime lover Vita Sackville-West. Following the outline of the book and weaving much of the beauty of the prose into the play, Elton Townend Jones has crafted a play that both does justice to the original text, as well as to the strengths of his Dyad counterpart, performer Rebecca Vaughan.</p>
						
						<p>It's fair at this stage to say that Vaughan has earned the right to be considered a veteran of the Fringe, an actor capable of flitting from joy to sadness in a moment and guiding the audience along the emotional journey of the story as deftly as through the vignettes of the tale. Her turns and mannerisms in Orlando move from boastful masculine follies of youth to a wiser femininity and through into a timeless yet sylphlike figure.</p>
						
						<p>Indeed Dyad Productions has visited the Edinburgh Festival Fringe for ten years, but if Orlando has anything to teach us it is that every step, every meeting and each romance and heartbreak is part of a journey. If you are already part of Dyad's journey, then you'll understand; if not, then there is no better place to meet them and fall in love yourself.</p>
