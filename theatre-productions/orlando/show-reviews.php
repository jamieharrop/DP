						<p><em>Orlando</em> at Assembly Roxy – Upstairs sees an always welcome return to The Edinburgh Fringe Festival by Dyad Productions.  Quality performances and wonderful words and stories have always been a feature of any past Dyad Productions performance and this year (their 10th anniversary with Assembly) maintains that high standard with this re-working of Virginia Woolf's "Orlando".</p>

						<p>Written and directed by Elton Townend Jones, and brought to life by the outstanding performance skills and oratory of Rebecca Vaughan as Orlando, we are taken on a centuries spanning story from Elizabethan England to present day as, over the course of a very long life, immortal and deep searching questions of personal identity, sexuality, gender identity, fame, money, status, life, death and much more are explored... so many questions and so few answers (if any).</p>
						
						<p>Orlando is a very complex character, born into wealth and status, a poet searching for immortal truths, and what we would now define as gender-neutral sexuality. With great skill as both a performer, and a storyteller, Rebecca Vaughan brings Orlando to vivid life with nothing more than words, mannerisms and body language.  Our set is absolutely minimal, so it is through the performance skills of Rebecca Vaughan and the script from Elton Townend Jones that this performance truly becomes "theatre of the mind" and we as an audience are given the largest stage of all upon which to imagine Orlando's world, our own minds.</p>
						
						<p>Orlando is a strange piece of work insomuch that it is not only truly timeless, but somehow seems even sharper and more relevant to our world today than when it was first written. In a world where bad language and endless references to bodily functions are desperately trying to be classed by some people as "Art", Dyad Productions are a constant reminder that the true art in theatre lies (as it has always done) in words and performances of magical power to take an audience on a journey of imagination and discovery.</p>
						
						<p>Absolutely nothing to do with this show, but if you are a Dr Who fan, and are curious as to what the future may hold for our favourite Time-Lord with a new gender identity, then <em>Orlando</em> offers more than a few intriguing possibilities of what a long longevity character of changing sexuality might be like in thoughts and mannerisms.</p>
