						<p>Full disclosure: I love Woolf's novel, and I confess to feeling sceptical about a stage adaptation. I've never been happier to be proven wrong, as Dyad Productions' script is outstanding. Shifting to a first-person monologue feels like a natural step, rendering Orlando's confusion palpable: torn between living a vibrant life, and striving to capture its essence through poetry. Special mention must go to Rebecca Vaughan's performance. Never less than magnetic, she perfectly captures the nuances of the character in a portrayal every bit as subtle and complex as Orlando's own struggles with ideas of self, identity and gender. At the gripping climax, tears were rolling down Vaughan's face, mirrored in the eyes of much of the audience, this cynical reviewer among them.</p>
