						<p>Rebecca Vaughan (Austen's Women, Jane Eyre: An Autobiography) gives a towering performance in Elton Townend Jones's adaptation of Virginia Woolf's 1928 novel. Orlando is the luscious, sweeping tale of a callow youth who dreams of being a great poet, who is granted immortality at the hand of an ageing Queen Elizabeth I, and becomes a witness to the next four centuries, shifting in gender as easily as he shifts in time.</p>
						
						<p>Orlando, with his 365-bedroom ancestral home and inherited fortune, becomes a dashing young courtier at the Elizabethan court. Later, having survived an uprising in Constantinople by escaping with mountain gypsies, he makes his way back to London, where "he" morphs into "she"</p>
						
						<p>The world of the play is evoked very simply with a chair and mirror draped in dust sheets.  Subtle changes to Vaughan's costume, by costume designed Kate Flanaghan, indicate the changing centuries. But, in truth, the entire show turns on her performance. Capable of being both assured and vulnerable, she grasps the rich language of the play with such confidence and clarity that this lengthy (for the Fringe) monologue is never dull.</p>
						
						<p>In a world where increasing numbers of people identify as non-binary, the fluidity of gender in <em>Orlando</em> takes on a new energy.</p>
						
						<p>We also see the centuries from a female perspective: the Victorian age passes in a dark cloud of stuffy crinolines and respectable marriages; only the modern age brings a measure of freedom and (at last) writing success.</p>
						
						<p>But perhaps the greatest achievement of the play is that Jones and Vaughan offer us – as Woolf does – glimpses of Orlando's inner world: haunted by an awareness of the passing of time despite having drunk the elixir of youth, and questing endlessly, through centuries, places and genders, for the truths of the shifting self.</p>
						
						<p>(This review was also subsequently published in The i Newspaper)</p>
