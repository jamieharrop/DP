						<p>Witty, defiant, brilliant Jane: an idol for generations of young women, and now, in true Edinburgh Fringe style, the centre of a one-woman show.</p>

						<p>Brilliantly performed by Rebecca Vaughan, Elton Townend Jones' adaptation might be stripped back, but it doesn't lose any of the atmosphere, warmth and sparkling chemistry of Jane's tumultuous relationship with Mr Rochester. However, it also gives us what some lesser adaptations can miss – Jane's astute perspective on the constraints of life as a woman two centuries ago.</p>

						<p>Abused by her aunt, and told to learn "shame and sobriety" at school, Jane is constantly battling against people trying to punish her, as a woman, for doing anything other than fade into the background. "They see demons but we are divine," as she puts it. And then there are the many supporting characters, so often forgotten in favour of Rochester and his "mad" wife in the attic: Jane's childhood idol Helen Brown; Grace Poole with her "goblin laugh"; Blanche Ingram with her "olive skin, raven hair [who] delighted at the piano" but quotes all of her opinions "from books".</p>

						<p>Through simple but striking, unobtrusive set design (and, in particular, atmospheric lighting from Martin Tucker), the cosy atmosphere of Thornfield, where Jane ends up as a governess, is conjured up from the shadows. "Tea, toast and a good sized teacake" are always on offer and the red glow of the fire ever present. It's here that Jane meets Rochester and a relationship develops that's not only a soaring romance, but a meeting of two sharp minds, both trying, in their own way to break down society's expectations for them. And what a relationship it is. While Rochester's insane wife isn't really given a voice, this is – as Charlotte Brontë wrote it – Jane's story, and it's very well done.</p>
