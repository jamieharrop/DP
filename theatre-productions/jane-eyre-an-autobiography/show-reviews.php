						<p>What a Herculean task it seems in theory to take on Charlotte Bronte's beloved novel and adapt it as a one-woman show! Miraculously it succeeds in its ambition. As the book itself is seen through Jane's eyes as a first person narrative this makes it perhaps a little easier but it's still no mean feat to take on one of literature's seminal heroines in this fashion singlehandedly and do it so well. The 'Reader' Jane addresses so intimately in the novel becomes 'Friends' on stage and the transition works well, it's a simple device but lends an air of shared experience.</p>

						<p>With just a simple, well-lit couch at her disposal Rebecca Vaughan manages to capture the essence of the novel from the first few minutes and brings all the key moments of the book vividly to life opening with Jane hiding from her violent cousin in the windowseat, her challenging time at Lowood Institution, her arrival at Thornfield and subsequent relationship with the brusque Mr Rochester plus everything that follows. Elton Townend Jones, writer and director of the show has done a terrific job in condensing the book in such a stylish and yet effective manner, nothing pivotal is overlooked, nothing superfluous is included.</p>

						<p>Excellent lighting and atmospheric soundscape are all the props Vaughan has, she conjures Bronte's unforgettable world simply with the power of her delivery and the vivacity with which she conveys the claustrophobic restrictions of Jane's existence 'I am no bird and no net ensnares me: I am a free human being with an independent will' it's something contemporary women take for granted of course but extraordinary stuff given that this was written in 1847.</p>

						<p>Of course Vaughan also has to embody every other character in the book and it's superbly done overall and by the enthusiastic reception she receives after a mere ninety minutes the audience are clearly captivated by her Jane Eyre.</p>
