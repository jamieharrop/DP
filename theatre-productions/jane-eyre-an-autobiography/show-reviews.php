						<p>There's a lot to say about Dyad Productions' one-person version of Jane Eyre: An Autobiography. There's the script, for starters, which manages to package the whole of Charlotte Brontë's first-person narrative into the space of an hour-and-a-half. It's an extremely sensitive job by Elton Townend Jones, who manages to cram all 38 chapters in without it ever feeling rushed, squashed or misshapen.</p>

						<p>Like the novel, we begin in medias res, so we move off at a fair lick. But it's pacy without ever taking on the frenetic feel of a One Man Lord of the Rings, or its ilk. Indeed, Jones isn't afraid to linger when necessary, or skip by when it works. Thus, we remain at Lowood long enough for the emotional and physical privations to leave their mark; but our time in the company of St. John is by contrast perfunctory and cold. The staging, too, is smartly devised. Minimalist and making excellent use of lighting, this goes nowhere near the excesses of period drama, and it's to the good.</p>

						<p>But, mostly, it's a solo performance that's most worth talking about. As Jane Eyre—as well as Mrs Reed, Bessie Lee, Mr Lloyd, Mr Brocklehurst, Mrs Fairfax, St. John Rivers, Bertha and, of course, Mr Rochester—Rebecca Vaughan is fantastic. She picks out character traits and accents without ever approaching caricature; she beautifully blurs the boundaries between Jane's observations and her reflections on them; she lulls us into an unquestioning reliance on her own narration. Her Jane is sympathetic and steely. Her Rochester is deeply alluring. There's a glib term which would aptly describe a one-woman peformance of this range. But to use it would be clichéd, which this production never is.</p>
