						<p>In 1836 aged just twenty, Charlotte Brontë sent a sample of her poetry to the Poet Laureate, Robert Southey, who was far from encouraging: "Literature cannot be the business of a woman's life, and it ought not to be."</p>

						<p>Undeterred by this misogynist response and several rejections from publishers, her novel, "Jane Eyre, an Autobiography", edited by Currer Bell was published in October 1847. Believed to be a true memoir revealing radical, feminist opinions on women in society, love, marriage and freedom, it was an instant success.</p>

						<p>Following "Dalloway" in 2014, Dyad's exquisitely elegant adaptation of Virginia Woolf's Mrs Dalloway, this innovative company has created another theatrical masterpiece, transferring Bronte's novel from page to stage with extraordinary imagination and literary insight.</p>

						<p>The empty stage is cloaked in a huge sail as a backcloth from ceiling and across the floor, on which is placed a small Ottoman. Amidst a soft shower of grey-tinted light and the sound of rain, Jane sits in her tightly corseted grey gown. We soon learn about her awful situation, living with a cruel aunt, and bullying cousins, before being sent off to Lowood, a charity school for orphans, who suffer a strict doctrine of privation, piety and punishment.</p>

						<p>As a narrator of her own autobiography, Rebecca Vaughan plays Jane throughout as an adult, reminiscing her childhood, school and friendship with Helen Burns – captured in evocative flashbacks. She studies hard to ensure an independent life and career as a governess, gaining employment at Thornfield Hall.</p>

						<p>Short dramatised vignettes feature a household of characters, the kindly Mrs Fairfax and the stern Mr Rochester, simply portrayed by adopting a low voice and stooped posture, right hand behind her back; Grace Poole, the strange servant, and dinner party guests including the rather dull, dumb socialite, Blanche Ingram whom Jane dismissingly refers to as "not original, nothing blooms, I am better than she!."</p>

						<p>The first real hint of her rising jealousy and affection for Edward Rochester, whom she now sees has hidden intellect and charm. Their conversations are sprinkled with humour: " Miss Ingram is a rare one, is she not?" he asks Jane, who politely replies, "Yes Sir," but in the cold tone and fury of a woman scorned.</p>

						<p>Intimate scenes are dramatised with atmospheric sound and lighting from a sunlit garden to a candlelit dining room with shimmering shadows on the back wall. Dinner bells and chiming clocks create a steady, timely pace day by day with the silence of the night broken by terrifying screams which chill the blood.</p>

						<p>This is a deftly distilled adaptation by Elton Townend Jones of Bronte's Gothic tale of mystery and romance. Jane Eyre and all the characters are brought to life with subtle nuance, virtuosity and heartfelt passion by Rebecca Vaughan. A cast of ten actors could not have created a more powerful or emotional production.</p>
