						<p>Jane Eyre – An Autobiography has to be one of the most moving pieces of theatrical storytelling ever created; quite simply, it's astounding. Hands down the best adaptation of Jane Eyre I have ever seen, in any medium. And there have been quite a few.</p>

						<p>Fiercely loyal to the novel, Jane Eyre – An Autobiography has been adapted by Elton Townend Jones, retaining much of the original text, to be performed by the unbelievable talent of Rebecca Vaughan. She plays Jane, fully formed, telling her story from childhood at Reed Hall to time at Thornfield, her escape to Moor House and her final return to her lover.</p>

						<p>Vaughan plays all the roles, using vocal variety and her skill in physical theatre to her advantage. This was done so expertly and seamlessly that it is possible to forget you were watching one single actress, and, instead, are able to lose yourself in the expert and evocative storytelling. The narrative was told with complete commitment and conviction, and Vaughan embodied the character of Jane so fully and realistically, bringing out all her humour and wit and independence.</p>

						<p>Rochester is not portrayed as a typical romantic lead, with all of his Byronic flaws being brought out in the open instead. His portrayal is very loyal to the book, and yet aspects that one may have skimmed over in reading are brought out: his manipulations, his jealousies… The unlikeable qualities managed to make the tale more realistic and powerful, however, and the interpretations of the novel were absolutely convincing throughout.</p>

						<p>Jane Eyre- An Autobiography is a fantastic introduction to Bronte's masterpiece, but for those who know and love the novel, it is a show not to be missed. The performance is powerful, polished and absolutely unique, breathing life into the characters like nothing I've ever seen before. </p>
