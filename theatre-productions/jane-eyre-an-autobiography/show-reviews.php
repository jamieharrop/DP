						<p>While that unfamiliar extended title may suggest a new spin on a literary classic (a staple tactic of Fringe theatremakers), rest assured: this is the same Charlotte Brontë-penned gothic romance you (possibly) know and love, adapted by Dyad Productions under its original title. It follows the life of Jane Eyre who, after a dismal childhood of abuse at the hands of a vicious aunt and a pious schoolmaster, uses her smarts to land a cushy job as governess of Thornfield Hall. There, she tutors a young girl, Adèle Varens, whose guardian Mr Rochester proves a volatile but alluring figure...</p>

						<p>Call us overcautious, but we'll avoid any further spoilers for a 169-year-old book. Sole performer Rebecca Vaughan pours her soul into making Jane an emotionally sympathetic hero for the audience to follow, and writer / director Elton Townend-Jones has done a good job of condensing the novel into 90 gripping minutes, and the staging – an off-white backdrop and couch, transformed by lighting cues – is elegantly simple.</p>					
