						<p>It's no surprise that Dyad Productions' latest theatrical offering could be described as minimally staged, as from the moment the lights rise from the darkness, to the final bow, the audience has been mesmerised completely by Rebecca Vaughan's magnetic stage presence and captivating control.</p>

						<p>The piece retells the story of Charlotte Brontë﻿'s arguably best work, Jane Eyre, by a fine adaptation from Vaughan's long-time collaborator Elton Townend Jones. It's a brilliant accomplishment in and of itself to have brought a three-volume novel to the stage in a mere ninety minutes; but what sets this production apart is the skill with which this solid script brings to the stage the story of Jane's life from child to grown woman, without ever flagging throughout the runtime.</p>

						<p>It's a marathon of a story, which still somehow never feels its own length and, of course, in the hands of a practiced and charismatic performer such as Vaughan, we're led on a bleak but captivating gothic journey, from the abuses and unkindnesses of Jane's youth, through troubles and tragedies as a schoolgirl to her place as a governess at Thornfield Hall and her brooding romance with Mr Rochester.</p>

						<p>It can be no simpler put that if you haven't seen Dyad Productions' work before, you simply must. They are a dependable diamond of the stage every Edinburgh Fringe Festival and should not be missed.</p>
