						<h4>
							Fringe Review<br />
							OUTSTANDING SHOW
						</h4>

						<p>Jane Eyre is written and directed by Elton Townend-Jones and stars Rebecca Vaughan, a writer and performer who repeatedly shines at the Fringe, and around the UK. Her solo shows are regularly acclaimed, often involving characters from literature and history.</p>	

						<p>I was more than a little dubious as to whether Charlotte Bronte's much loved novel could be adequately covered in a 90 minute piece of theatre. Indeed, the fact that this was to be done as a one woman show increased my scepticism. However, these doubts were soon laid to rest, as Dyad Productions have not only managed it, but have produced an incredible show which authentically reproduces the novel onto the stage.</p>	

						<p>Although it is a very basic set with only a single couch set centre, it works extremely well and with the clever use of lighting and soundscape, allows the narrative to flow quickly without ever feeling rushed or clunky and manages to conjure up the different moods that proliferate the novel.</p>	

						<p>I felt the novel had come to life on the stage, be it the red room, Lowood or any of the numerous settings. So, it looks and feels right, but there are two main reasons why this is such a brilliant piece of theatre – firstly Elton Townend-Jones remarkable adaption and direction, which somehow fits in all the essential elements of the novel without it feeling forced or rushed, is simply remarkable.</p>	

						<p>It also remains faithful to Bronte's novel, everything being seen through Jane Eyre's eyes without adding unnecessary scenes that never appeared in the novel, but are often added in film and other adaptations detracting from the essence of the original work. Secondly, there is the actor, a simply stunning performance from Rebecca Vaughan, playing 24 different characters, each with their own distinctive personality, which gave me the same feelings and experience as an audience member as the novel did when I read it. Whether it be the nasty John Reid, sympathetic Bessie, the delightfully batty Miss Fairfax or any of the vast array of people portrayed, each adds to the narrative and contributes to a feeling of being immersed in the novel.</p>	

						<p>It is not only a singular feat of memory, but to bring the page to the stage so convincingly is something I will not quickly forget and begs a second viewing. Attention to detail is very evident in all aspects of this production, like Vaughan's costume and hairstyle which are exactly how I pictured Jane Eyre from the novel, and thus helps one to be drawn into that 19th century world.</p>	

						<p>This adaptation stands on its own and whether or not you are familiar with the story of Jane Eyre or completely unaware, this is an enjoyable and absorbing journey. As I left the theatre I heard numerous positive comments and there was a definite feeling that we had just seen something very special.</p>	

						<p>Dyad Productions have produced a number of well received shows over a few years now, but this, I believe, has reached a new level and I not only am I planning to see Jane Eyre again, I thoroughly look forward to their next project.</p>					
