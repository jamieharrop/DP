						<p>If you are looking for a show that will charm all of the family, this could be the one.</p>

						<p>The Diaries of Adam and Eve combines a good original premise, Mark Twain's wit updated in an anglicised adaptation by Elton Townend Jones, who is also Adam, and good acting under the sure direction of Guy Masterson.</p>

						<p>We all know about Adam and Eve and the familiar plotlines are all there, delivered from novel angles that regularly raise laughs. The pair live in an English country garden that is positively Edenic.</p>

						<p>Adam is a slob who spends most of his time reading Metro and dozing in his deckchair. His partner, born an adult, is Adam's polar opposite.</p>

						<p>Rebecca Vaughan's Eve is interested in every aspect of the world, with the instincts of a naturalist and physicist rolled into one. Unfortunately for poor Adam, she also feels an intrinsic need to share any piece of information that she learns or creates, driving him to distraction.</p>

						<p>As an actor, Townend Jones plays second fiddle to the overpowering Eve but still gets his share of laughs.</p>

						<p>This is all great fun and the humour rarely lets up through an enjoyable 75 minutes.</p>
