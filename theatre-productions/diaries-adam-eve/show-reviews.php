						<p>This well-acted two hander is comic and poetic, thoughtful and polished. It shows Adam and Eve getting used to each other and to the world around them in a complex and interesting way that makes for compelling theatre.</p>

						<p>The play is a two-hander adapted from Mark Twain's short stories and very ably acted by Elton Townend Jones and Rebecca Vaughan. It shows Adam and Eve's first days in the Garden of Eden together, and how they struggle to build a relationship with each other and the world around them. It uses comedy to address the gender politics between them – Eve on Adam: ‘It's difficult to tell quite what it is you're for'; Adam on Eve: ‘Don't make the water come out of those places you look out of again!' It is a well-acted, thoughtful comedy.</p>

						<p>The script was written by Elton Townend Jones (who also plays Adam), adapted from Mark Twain's short stories ‘Extracts from Adam's Diary' and ‘Eve's Diary'. The language is comic as well as lyrical, and the lines beautifully constructed (One of Eve's early lines is the superbly phrased ‘There are too many stars in some places and not enough in others – but that can be remedied presently, no doubt'). Considering it is taken from prose in the form of a diary it is never static or verbose – there are lots of brilliantly devised non-verbal sequences of the story.</p>

						<p>The acting from Rebecca Vaughan (who has acted in Dyad Production's previous shows Austen's Women and I, Elizabeth) and Elton Townend Jones was faultless. It was clearly very well rehearsed and directed.  There were some beautifully devised sequences, such as a rapid section of business with thermoses where Eve copies Adam's movements in close succession. Their dialogue delivery was just as well-rehearsed and carefully thought out – Vaughan's rapid-fire delivery of Eve's long stream of consciousness speeches were impeccably acted and impressively clear.</p>
						
						<p>The quality of the acting and the language render this an impressive show. I am therefore awarding the show a highly recommended 4 stars.</p>
