						<p>Loosely based upon a series of short stories by Mark Twain about Adam and Even in the Garden of Eden, this adaptation by Elton Townend Jones is pitched somewhere between a romantic comedy and a ‘men are from Mars, women are from Venus'-style self help book.</p>

						<p>The two-hander is jolly and entertaining and plays into all the gender stereotypes one would expect to find in a Hollywood rom-com. Rebecca Vaughan makes a suitably enthusiastic, clingy Eve, while Elton Townend Jones convinces as a stolid, withdrawn and inexpressive Adam. The pair of lovers squabble and fight, but in the end – of course – they come to appreciate the other's qualities. Simply staged with a couple of deckchairs and pot plants, Guy Masterson's direction keeps the action ticking along and there are nice one-liners. "I am determined," says Eve. "You are right now, but if you eat that [apple], it's free will all the way," quips Adam. Gently amusing stuff.</p>
