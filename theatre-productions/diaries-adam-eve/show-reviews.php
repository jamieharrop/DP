						<p>If dealing with relationships between man and woman, both loving and confrontational, why not go all way back to the mythical first partnership? Writer Elton Townend Jones, who also appears as Adam, has successfully adapted the writings of Mark Twain's ‘Diaries of Adam and Eve' written separately and published over a number of years to produce a modern composite version of the story. In association with the excellent Rebecca Vaughan (Eve) and Director Guy Masterson, a lively and witty play has resulted.</p>

						<p>For much of the play it is the confrontation which is emphasised. Eve is loquacious, words pouring out of her mouth with excitement as she invents words, discovers concepts and even fire. Adam, on the other hand, simply wants to spend most of his time lolling around in peace and quiet. Despite Eve's warmth towards him, he is not interested. He does invent one word – superfluous – but that is meant to be a put down to Eve.</p>

						<p>Real discord develops when Eve takes the first bite of the apple from the Forbidden Tree. However, their relationship changes when Adam is persuaded to take a bite as well. It takes on a sexual nature and Cain and Abel are the results. The play draws to a conclusion with Eve making a moving speech in praise of her man despite all his faults and weaknesses.</p>
