						<p>It's the basis of every love story, every romantic ballad, and every sentimental poem ever written; man meets woman, they disagree, they resolve their differences, they fall in love.</p>

						<p>However, it helps no end if your play is based on an original short story by Mark Twain.  In adapting it for the stage, the writer  Elton Townend Jones (who also plays Adam) has generously resisted the temptation to keep all the good lines for himself.</p>

						<p>When the piece opens, Adam is a grumpy solitary in a deckchair, enjoying being the only man in the Garden of Eden and indeed the world, when his peace is invaded by a deliriously irritating Eve, who proceeds to name everything, make friends with the animals, and generally drive Adam to distraction. She is clearly the forerunner of every "jolly hockey sticks" chatty and giggly girl comedian in the world. He eventually, much against his better judgement, decides that he does in fact find her quite comely and shapely, although he has serious reservations about all this talking business!</p>

						<p>In this case, there is no doubt how the story will turn out, and it's the journey itself which is sheer pleasure, graced throughout with sparky and very funny dialogue.</p>

						<p>Eve is every Guinevere, Scarlett O'Hara, and Ilsa who ever lived, while Adam is every Lancelot, Rhett Butler, and Rick Blaine.  The closing minutes are deeply moving, and the final words spoken by Adam may be the most loving and heartfelt spoken on any stage this festival, or indeed any other.</p>
