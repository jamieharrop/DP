								<h4>Elton Townend Jones - <small>Producer / Actor / Writer / Director</small></h4>

								<p>Elton wrote, directed and performed in his first play – <i>Origen</i> – aged 18 at King's Lynn Arts Centre, whilst studying Performing Arts in East Anglia.  During this time, he co-created cult comedy character William Whicker, with comedian Waen Shepherd, after which he spent five years as a professional stage and TV actor.</p>

								<p>His first professional role was 'Salieri' in The Cockpit's <i>Amadeus</i>. TV appearances include: <i>Lovejoy</i>, <i>The Chief</i>, and <i>Silent Witness</i>.</p>

								<p>He subsequently gained a BA in Literature with Philosophy and went on to run a popular regional theatre, increasing attendance by 33% in just six months.  He directed and acted in professional tours of Shakespeare and Godber, writing, directing and appearing in the short film '<i>Dear Eve...</i>', and penning <i>Cutting the Cord</i> at Battersea Arts Centre.</p>
